EStatus ErrnoToEStatus(int s32Errno)
{
	UINT32 u32Loop;
	
	// See if we can find an equivalent errno
	for (u32Loop = 0; u32Loop < (sizeof(sg_sErrnoEstatusTable) / sizeof(sg_sErrnoEstatusTable[0])); u32Loop++)
	{
		if (s32Errno == sg_sErrnoEstatusTable[u32Loop].s32ErrnoValue)
		{
			return(sg_sErrnoEstatusTable[u32Loop].eStatus);
		}
	}


	return(ESTATUS_ERRNO_UNKNOWN);
}

// Translates a 0/-1 style of result to an eStatus in case of error
EStatus POSIXToEStatus(int s32Result)
{
	EStatus eStatus;

	if (0 == s32Result)
	{
		// All good
		eStatus = ESTATUS_OK;
	}
	else
	if (-1 == s32Result)
	{
		// Error of some sort
		eStatus = ErrnoToEStatus(errno);
	}
	else
	{
		// Unknown result code
		printf("Result code=%d\n", s32Result);
		BASSERT(0);
		eStatus = ESTATUS_UNKNOWN_RESULT_CODE;
	}

	return(eStatus);
}

